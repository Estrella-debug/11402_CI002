#include <iostream>

using namespace std;

int main() {
    int N;
    // 使用 while 迴圈持續讀取輸入的初始可樂數量 N，直到測資結束
    while(cin >> N) {
        /* 公式解：
           1. 每 3 個空瓶換 1 瓶新可樂，喝完又剩 1 個空瓶，實際上是「花掉 2 個空瓶換到 1 瓶內容」。
           2. 因為題目允許「借空瓶」，只要最後能還回去，這代表只要你有 2 個空瓶，
              就能跟朋友借 1 個湊齊 3 個去換，喝完後剛好剩 1 個還給朋友。
           3. 總數 = 原始 N 瓶 + (N / 2) 瓶額外換到的。
           4. 數學整理後：N + N/2 = (2N + N) / 2 = 3N / 2。
        */
        cout << (3 * N) / 2 << endl;
    }
    return 0;
}